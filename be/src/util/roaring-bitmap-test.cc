vector<uint64_t> ExtractValues(const RoaringBitmap64& bitmap) {
  vector<uint64_t> values;
  RoaringBitmap64::Iterator it(bitmap);
  while (it.HasValue()) {
    values.push_back(it.Value());
    it.Advance();
  }
  return values;
}

void CheckDeserialize(const vector<uint8_t>& serialized_bitmap,
    const vector<uint64_t>& expected_values) {
  RoaringBitmap64 bitmap;
  ASSERT_OK(RoaringBitmap64::Deserialize(
      serialized_bitmap.data(), serialized_bitmap.size(),
      &bitmap));
  auto actual_values = ExtractValues(bitmap);

  ASSERT_EQ(expected_values, actual_values);
}

