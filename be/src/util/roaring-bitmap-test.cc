void CheckDeserializeFailure(const vector<uint8_t>& serialized_bitmap,
    const string& expected_error) {
  RoaringBitmap64 bitmap;
  Status status = RoaringBitmap64::Deserialize(
      serialized_bitmap.data(), serialized_bitmap.size(),
      &bitmap);
  ASSERT_EQ(status.msg().msg(), expected_error);
}

TEST(RoaringBitmap64Test, DeserializeFailure) {
  CheckDeserializeFailure({
      0x00, 0x00, 0x00, 0x00, 0x00, 0xFF, 0x00, 0x00},
      "Deserialization of roaring bitmap failed.");
  CheckDeserializeFailure({
      0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x3a, 0x30,
      0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00, 0xFF, 0x00, 0x01, 0x1F, 0xFF , 0xFF, 0x00,
      0x00, 0xFF},
      "Deserialization of roaring bitmap failed.");
  // Modified bitmap of [0, 1, 2] to have elements [1, 0, 2] in the serialized array.
  CheckDeserializeFailure({
      0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x3a, 0x30,
      0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x02, 0x00, 0x10, 0x00, 0x00, 0x00,
      0x01, 0x00, 0x00, 0x00, 0x02, 0x00},
      "Deserialization of roaring bitmap failed with reason: array elements not strictly"
      " increasing");
}

void CheckSerializeDeserialize(const vector<uint64_t>& values) {
  RoaringBitmap64 orig_bitmap;
  orig_bitmap.AddElements(values);

  vector<uint8_t> buffer(orig_bitmap.BitmapSizeInBytes());
  orig_bitmap.Serialize(buffer.data());

  RoaringBitmap64 deserialized_bitmap;
  ASSERT_OK(RoaringBitmap64::Deserialize(
      buffer.data(), buffer.size(), &deserialized_bitmap));
  ASSERT_EQ(values, ExtractValues(deserialized_bitmap));
}

TEST(RoaringBitmap64Test, SerializeDeserialize) {
  CheckSerializeDeserialize({});
  CheckSerializeDeserialize({0});
  CheckSerializeDeserialize({1});
  CheckSerializeDeserialize({100000});
  CheckSerializeDeserialize({10000000000});
  CheckSerializeDeserialize({1000000000000});
  CheckSerializeDeserialize({std::numeric_limits<uint64_t>::max()});
  CheckSerializeDeserialize({31, 32, 33, 34, 35, 36, 37, 38, 39});
  CheckSerializeDeserialize({0, 10000, 100000000000, 200000000000, 300000000000,
      400000000000, 500000000000, std::numeric_limits<uint64_t>::max()});
}

