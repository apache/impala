#pragma once

#include <opentelemetry/sdk/common/global_log_handler.h>

namespace impala {

// Custom log handler for OpenTelemetry SDK internal logs.
//
// Log messages generated by the OpenTelemetry SDK are dropped by default. This class
// implements the SDK's LogHandler interface to emit internal SDK log messages to Impala's
// logging system.
class OtelLogHandler : public opentelemetry::sdk::common::internal_log::LogHandler {
    void Handle(opentelemetry::sdk::common::internal_log::LogLevel level,
        const char* file, int line, const char* msg,
        const opentelemetry::sdk::common::AttributeMap &attributes) noexcept override;
};

} // namespace impala
