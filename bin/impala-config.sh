    if [[ "${IMPALA_JDK_VERSION}" == "8" ]]; then
      DETECTED_JAVA_HOME="${JVMS_PATH}/java-1.8.0"
    else
      DETECTED_JAVA_HOME="${JVMS_PATH}/java-${IMPALA_JDK_VERSION}"
    fi
  fi

